package statisticker;


public class Stats {

		public float average;
		public float min;
		public float max;
		
		public float getAverage() {
			return this.average;
		}
		public float getMin() {
			return this.min;
		}
		public float getMax() {
			return this.max;
		}
		public void setAverage(float average) {
			this.average = average;
		}
		public void setMin(float min) {
			this.min = min;
		}
		public void setMax(float max) {
			this.max = max;
		}
	}
